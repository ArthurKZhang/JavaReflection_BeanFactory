package com.arthur.reflect;

public class Arthur {
	private String arthurName;
	private String arthurSize;

	public String getArthurName() {
		return arthurName;
	}

	public void setArthurName(String arthurName) {
		this.arthurName = arthurName;
	}

	public String getArthurSize() {
		return arthurSize;
	}

	public void setArthurSize(String arthurSize) {
		this.arthurSize = arthurSize;
	}
}
